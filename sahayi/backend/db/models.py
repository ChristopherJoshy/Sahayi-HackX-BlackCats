class MemoryNote(Base):
    """Persistent memory note for companion personality and patient context.

    Args:
        None: ORM model fields are mapped columns.
    Returns:
        MemoryNote ORM class.
    Agent:
        Database
    """

    __tablename__ = "memory_notes"
    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    patient_id: Mapped[int] = mapped_column(ForeignKey("patients.id"), index=True)
    category: Mapped[str] = mapped_column(String(32), default="context")
    content: Mapped[str] = mapped_column(Text, default="")
    created_at: Mapped[datetime] = mapped_column(DateTime, default=datetime.utcnow)
    updated_at: Mapped[datetime] = mapped_column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)
    patient: Mapped["Patient"] = relationship(back_populates="memory_notes")


class MistakeNote(Base):
    """Logged AI mistake so the system can learn from its own errors.

    Stores what the model did, why it was wrong, the correction, and the
    lesson to apply next time. Optionally tied to a patient for context.

    Args:
        None: ORM model fields are mapped columns.
    Returns:
        MistakeNote ORM class.
    Agent:
        Database
    """

    __tablename__ = "mistake_notes"
    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    patient_id: Mapped[int | None] = mapped_column(ForeignKey("patients.id"), index=True, nullable=True)
    agent: Mapped[str] = mapped_column(String(48), default="orchestrator")
    mistake_type: Mapped[str] = mapped_column(String(48), default="general")
    context: Mapped[str] = mapped_column(Text, default="")
    error: Mapped[str] = mapped_column(Text, default="")
    correction: Mapped[str] = mapped_column(Text, default="")
    lesson: Mapped[str] = mapped_column(Text, default="")
    created_at: Mapped[datetime] = mapped_column(DateTime, default=datetime.utcnow)


class RelativeUpdate(Base):
    """Clinical summary of a relative's submission, surfaced to the doctor.

    Relatives may send prescription images, voice notes, or text over WhatsApp.
    To honour the dashboard privacy rule we store only a derived CLINICAL
    summary (never the raw transcript or personal message). Optionally tied to
    a patient; used by the doctor dashboard "Relative Updates" panel.

    Args:
        None: ORM model fields are mapped columns.
    Returns:
        RelativeUpdate ORM class.
    Agent:
        Database
    """

    __tablename__ = "relative_updates"
    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    patient_id: Mapped[int] = mapped_column(ForeignKey("patients.id"), index=True)
    relative_name: Mapped[str] = mapped_column(String(120), default="Relative")
    update_type: Mapped[str] = mapped_column(String(32), default="voice_note")
    clinical_summary: Mapped[str] = mapped_column(Text, default="")
    source_detail: Mapped[str] = mapped_column(String(255), default="")
    created_at: Mapped[datetime] = mapped_column(DateTime, default=datetime.utcnow)
