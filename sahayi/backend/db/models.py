"""SQLAlchemy ORM models for SAHAYI."""

from __future__ import annotations

from datetime import datetime

from sqlalchemy import JSON, Boolean, DateTime, Float, ForeignKey, Integer, String, Text
from sqlalchemy.orm import DeclarativeBase, Mapped, mapped_column, relationship


class Base(DeclarativeBase):
    """Base ORM class for all SAHAYI tables.

    Args:
        None: Declarative SQLAlchemy base.
    Returns:
        Base subclass for mapped models.
    Agent:
        Database
    """


class Patient(Base):
    """Patient master record.

    Args:
        None: ORM model fields are mapped columns.
    Returns:
        Patient ORM class.
    Agent:
        Database
    """

    __tablename__ = "patients"
    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    name: Mapped[str] = mapped_column(String(120), nullable=False)
    language: Mapped[str] = mapped_column(String(20), nullable=False, default="ml-IN")
    phone_number: Mapped[str] = mapped_column(String(32), unique=True, index=True)
    registration_number: Mapped[str | None] = mapped_column(String(64), unique=True, index=True, nullable=True)
    conditions: Mapped[list[str]] = mapped_column(JSON, default=list)
    medicines: Mapped[list[dict]] = mapped_column(JSON, default=list)
    emergency_contact: Mapped[dict] = mapped_column(JSON, default=dict)
    doctor_contact: Mapped[dict] = mapped_column(JSON, default=dict)
    relatives: Mapped[list[dict]] = mapped_column(JSON, default=list)
    doctor_uid: Mapped[str] = mapped_column(String(128), index=True)
    doctor_email: Mapped[str] = mapped_column(String(255), index=True)
    created_at: Mapped[datetime] = mapped_column(DateTime, default=datetime.utcnow)
    call_sessions: Mapped[list["CallSession"]] = relationship(back_populates="patient", cascade="all, delete-orphan")
    signals: Mapped[list["SignalRecord"]] = relationship(back_populates="patient", cascade="all, delete-orphan")
    summaries: Mapped[list["DoctorSummary"]] = relationship(back_populates="patient", cascade="all, delete-orphan")
    memory_notes: Mapped[list["MemoryNote"]] = relationship(back_populates="patient", cascade="all, delete-orphan")


class CallSession(Base):
    """Voice or message session record.

    Args:
        None: ORM model fields are mapped columns.
    Returns:
        CallSession ORM class.
    Agent:
        Database
    """

    __tablename__ = "call_sessions"
    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    patient_id: Mapped[int] = mapped_column(ForeignKey("patients.id"), index=True)
    session_id: Mapped[str] = mapped_column(String(64), unique=True, index=True)
    started_at: Mapped[datetime] = mapped_column(DateTime, default=datetime.utcnow)
    ended_at: Mapped[datetime | None] = mapped_column(DateTime, nullable=True)
    risk_score: Mapped[float] = mapped_column(Float, default=0.0)
    status: Mapped[str] = mapped_column(String(20), default="active")
    patient: Mapped["Patient"] = relationship(back_populates="call_sessions")


class SignalRecord(Base):
    """Extracted signal record for one patient turn.

    Args:
        None: ORM model fields are mapped columns.
    Returns:
        SignalRecord ORM class.
    Agent:
        Database
    """

    __tablename__ = "signal_records"
    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    patient_id: Mapped[int] = mapped_column(ForeignKey("patients.id"), index=True)
    session_id: Mapped[str] = mapped_column(String(64), index=True)
    fatigue: Mapped[bool] = mapped_column(Boolean, default=False)
    appetite: Mapped[str] = mapped_column(String(64), default="unchanged")
    chest_pain: Mapped[bool] = mapped_column(Boolean, default=False)
    duration_days: Mapped[int] = mapped_column(Integer, default=0)
    severity: Mapped[int] = mapped_column(Integer, default=1)
    red_flag: Mapped[bool] = mapped_column(Boolean, default=False)
    symptom_description: Mapped[str] = mapped_column(Text, default="")
    source_text: Mapped[str] = mapped_column(Text, default="")
    confidence: Mapped[float] = mapped_column(Float, default=0.0)
    created_at: Mapped[datetime] = mapped_column(DateTime, default=datetime.utcnow)
    patient: Mapped["Patient"] = relationship(back_populates="signals")


class DoctorSummary(Base):
    """Doctor-facing summary generated by the agent stack.

    Args:
        None: ORM model fields are mapped columns.
    Returns:
        DoctorSummary ORM class.
    Agent:
        Database
    """

    __tablename__ = "doctor_summaries"
    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
